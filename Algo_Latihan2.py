# Setting Variabel Nilai 
a = 10 
b = 20 
c = 50

# Perhitungan 
hasil = ((a * b) * (b + c)) / (a + b + c)

# Hasil Perhitungan 
print("----------------------")
print("Masukkan nilai a:", a)
print("Masukkan nilai b:", b)
print("Masukkan nilai c:", c)
print("----------------------")
print("Hasil perhitungan: ", hasil)
print("----------------------")


