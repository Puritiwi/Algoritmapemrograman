# Setting Variabel
a = 10
b = 20
c = 50
p = a
l = b
t = c

# Rumus
A = ((a+b)*(b*c))/(a+b+c)
B = p*l*t
C = 1/2*a*t
D = (((a+b)**2)+((b*c)**2))/(a*b)

# Cetak Hasil
print("------------------------")
print("a = p=",a)
print("b = l=",b)
print("c = t=",c)
print("------------------------")
print("Hasil dari A =",A)
print("Hasil dari B =",B)
print("Hasil dari C =",C)
print("Hasil dari D =",D)
